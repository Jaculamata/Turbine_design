function P = PSK(T)
%***************************************
% 某温度下饱和压力函数
%  Parameters: P      压力   MPa 
%              t      温度   ℃ 
% ****作者：王雷 zrqwl2003@126.com*****************
%***************************************
 F=[0,-7.691234564,-26.08023696,-168.1706546,64.23285504,-118.9646225,4.16711732,20.9750676,1000000000,6];
zeros(1,10);
ZT=(T+273.15)/647.3;
ZTT=1-ZT;
PK=0.0;
for  IK=1:1:6
    I=7-IK;
    PK=PK*ZTT+F(I);   
end
PK=PK/ZT/(1+F(7)*ZTT+F(8)*ZTT^2);
PK=PK-ZTT/(F(9)*ZTT^2+F(10));
P=exp(PK)*221.20;
P=P/10;
%压力函数